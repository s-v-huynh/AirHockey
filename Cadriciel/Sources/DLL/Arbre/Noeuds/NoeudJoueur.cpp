#include "NoeudJoueur.h"

////////////////////////////////////////////////////////////////////////
///
/// @fn NoeudJoueur::NoeudJoueur(bool virtuel, std::string nom)
///
/// Le constructeur par parametre
///
/// @param[in] virtuel : booleen
///				nom : string
///
/// @return Aucun
///
////////////////////////////////////////////////////////////////////////
NoeudJoueur::NoeudJoueur(bool virtuel, std::string nom)
{ 
	nomJoueur_ = nom;
	estVirtuel_ = virtuel;
	maillet_ = nullptr;
	nbBut_ = 0;
}

////////////////////////////////////////////////////////////////////////
///
/// @fn NoeudJoueur::~NoeudJoueur()
///
/// Le destructeur
///
/// @param[in] Aucun
///
/// @return Aucun
///
////////////////////////////////////////////////////////////////////////
NoeudJoueur::~NoeudJoueur()
{

}

////////////////////////////////////////////////////////////////////////
///
/// @fn void NoeudJoueur::assignerMaillet(NoeudAbstrait* maillet)
///
/// Cette fonction assigne le maillet a un joueur
///
/// @param[in] maillet : NoeudAbstrait*
///
/// @return Aucun
///
////////////////////////////////////////////////////////////////////////
void NoeudJoueur::assignerMaillet(NoeudAbstrait* maillet)
{
	maillet_ = maillet;
}

////////////////////////////////////////////////////////////////////////
///
/// @fn int NoeudJoueur:: getBut() const
///
/// Cette fonction obtient le nombre de but
///
/// @param[in] Aucun
///
/// @return Le nombre de but (int)
///
////////////////////////////////////////////////////////////////////////
int NoeudJoueur:: getBut() const
{
	return nbBut_;
}

////////////////////////////////////////////////////////////////////////
///
/// @fn void NoeudJoueur::setBut(int but)
///
/// Cette fonction modifie le nombre de but
///
/// @param[in] but : int
///
/// @return Aucun
///
////////////////////////////////////////////////////////////////////////
void NoeudJoueur::setBut(int but)
{
	nbBut_ = but;
}

////////////////////////////////////////////////////////////////////////
///
/// @fn void NoeudJoueur::incrementerBut()
///
/// Cette fonction incremente le nombre de but
///
/// @param[in] Aucun
///
/// @return Aucun
///
////////////////////////////////////////////////////////////////////////
void NoeudJoueur::incrementerBut()
{
	nbBut_++;
}

////////////////////////////////////////////////////////////////////////
///
/// @fn void NoeudJoueur::reinitialiserBut()
///
/// Cette fonction remet le nombre de but a 0
///
/// @param[in] Aucun
///
/// @return Aucun
///
////////////////////////////////////////////////////////////////////////
void NoeudJoueur::reinitialiserBut()
{
	nbBut_ = 0;
}